public class TwoDiArray {
    public static void main(String[] args) {
        int [][] nums = {{1, 2, 3},
                         {4, 5, 6}};

        //            0      1     2
        // 
        //      0    "1"   "2"   "3"
        // 
        //      1    "4"   "5"   "6"
                
         
        System.out.println(nums[1][2]);
    }
}
